/**
 * <h1>Calculadora</h1>
 * A calculadora realiza operações matemáticas entre numeros inteiros.
 * <p>
 * <b>Nota:</b> Leia atentamente a documentação desta classe para desfrutar dos recursos oferecidos pelo outor.
 * 
 * @author William
 * @version 1.0
 * @since 2024       
 */
public class Calculadora {
    /**
     * Este método realiza a soma de dois números inteiros.
     * 
     * @param a Primeiro número inteiro
     * @param b Segundo número inteiro
     * @return A soma dos dois números inteiros
     */
    public int somar(int a, int b) {
        return a + b;
    }

    /**
     * Este método realiza a subtração de dois números inteiros.
     * 
     * @param a Primeiro número inteiro
     * @param b Segundo número inteiro
     * @return A subtração dos dois números inteiros
     */
    public int subtrair(int a, int b) {
        return a - b;
    }

    /**
     * Este método realiza a multiplicação de dois números inteiros.
     * 
     * @param a Primeiro número inteiro
     * @param b Segundo número inteiro
     * @return A multiplicação dos dois números inteiros
     */
    public int multiplicar(int a, int b) {
        return a * b;
    }

    /**
     * Este método realiza a divisão de dois números inteiros.
     * 
     * @param a Primeiro número inteiro
     * @param b Segundo número inteiro
     * @return A divisão dos dois números inteiros
     */
    public int dividir(int a, int b) {
        return a / b;
    }

  

  
}